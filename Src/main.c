/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{
  /* USER CODE BEGIN 1 */

  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_DMA_Init();
  MX_ADC1_Init();
  MX_CAN_Init();
  MX_TIM1_Init();
  MX_TIM2_Init();
  MX_TIM3_Init();
  /* USER CODE BEGIN 2 */

  //setup servos
  for(int i=0; i<N_CHANNELS; i++)
    setServo(i, SERVO_CENTER);
  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  uint32_t txMailbox;
  while (1)
  {
    HAL_GPIO_WritePin(status_GPIO_Port, status_Pin, GPIO_PIN_SET);

    //transmit status
    if (HAL_CAN_GetTxMailboxesFreeLevel(&hcan) > 0){
            //set address
            static uint8_t TXCounter = 0;
            uint8_t data[2] = {0};
            txHeader.StdId = CANAddress | 0x8 | (TXCounter & 0b111);

            //fetch correct data
            switch(TXCounter){
                case 0: //battery voltage
                    data[1] = ADCResults[0] & 0xff;
                    data[0] = (ADCResults[0]>>8) & 0xff;
                    break;
                case 1: //battery current
                    data[1] = ADCResults[1] & 0xff;
                    data[0] = (ADCResults[1]>>8) & 0xff;
                    break;
            }

            //send
            HAL_CAN_AddTxMessage(&hcan, &txHeader, data, &txMailbox);

            //next address
            TXCounter++;
            if (TXCounter > CAN_MESSAGES-1)
                TXCounter = 0;
    }

    HAL_ADC_Start_DMA(&hadc1, ADCResults, 2); // start adc in DMA mode

    //center any channel that hasn't been updated for a while
    for(int i=0; i<N_CHANNELS; i++){
        if ( (HAL_GetTick() - servoLastUpdated[i]) > SAFETY_TIMEOUT)
            setServo(i, SERVO_CENTER);
    }

    HAL_GPIO_WritePin(status_GPIO_Port, status_Pin, GPIO_PIN_RESET);
    HAL_Delay(1000/(CAN_MESSAGES*3));
    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
  }
  /* USER CODE END 3 */
}

